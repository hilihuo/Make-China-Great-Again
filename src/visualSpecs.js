export function getVisualSpec(node) {
  return visualSpecs[node?.id] || {
    scene: "archive",
    portrait: { headwear: "scholar-cap", robe: node?.color || "#5c4a32", prop: "book", mood: "observer" },
    artifact: { name: node?.artifacts?.[0]?.name, variant: node?.artifacts?.[0]?.geometry || "generic", motifs: [] }
  };
}

export function getArtifactVisualSpec(artifact) {
  const found = Object.values(visualSpecs).find((spec) => spec.artifact?.name === artifact?.name);
  return found?.artifact || { name: artifact?.name, variant: artifact?.geometry || "generic", motifs: [] };
}
